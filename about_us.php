<p class="p2"> INTRODUCTION TO THE UTTARANCHAL UNIVERSITY </p>
<p class="p3">Uttaranchal University is constituted with the merger of professional institutes of Sushila Devi Centre for Professional Studies & Research Society, namely Law College Dehradun, Uttaranchal Institute of Technology and Uttaranchal Institute of Management.

Uttaranchal University is one of the leading educational hubs of professional courses with innumerable students enrolled in different educational programs. It offers an array of multi–disciplinary courses making it a knowledge portal where excellence matters. In Uttaranchal University, we understand the potential of young minds and nurture them with passion.

Uttaranchal University can boast of being an institution par excellence. It is located in a fascinating geological setting. It is flanked by NH-72 on one side and a beautiful broad slithering river on the other side. The pine trees surrounding the area lend a pristine and wholesome ambience to the campus.

We offer a unique life, you will have access to comprehensive facilities, cultural activities, wide range of academic courses and industry interface</p><br />

<p class="p2">CHANCELLOR'S MESSAGE</p>
<p class="p3">The founders of Uttaranchal University envisaged an institution of learning which would transcend all barriers and become a university of national character in letter and spirit. Our emphasis is on diffusion of scientific, technical and professional knowledge on one hand and on the other hand building up the character of youth by integrating ethics with education.

I am happy to say that the University’s unique campus and nine interdisciplinary institutes- Law College Dehradun, Uttaranchal Institute of Technology, Uttaranchal Institute of Management, Uttaranchal Institute of Pharmaceutical Sciences, Uttaranchal College of Applied & Life Sciences, School of Agriculture, University Polytechnic, Uttaranchal College of Journalism & Mass Communication and School Of Hospitality Management are making their mark at the state and national level in the frontier areas of Law, Engineering, Management, Information Technology, Pharmacy, Applied & Life Sciences, Agricultural Science, Journalism & Mass Communication and Hotel Management. I hope the same for School of Liberal Arts.

The University is complemented by the pioneering efforts of more than a decade in legal education, engineering and management which has won us the place among the premier institutes of learning in the country. Our dedication to offer the best infrastructure and faculty has been amply rewarded by the excellent academic results achieved by our students. Among other accolades, our students have won prestigious awards from many Universities. A University can grow when research is combined with teaching. Therefore we have ensured the development of a collaborative environment conducive to learning, exposure to the best international practices and promotion of innovation and creativity.

<br>Our students will find learning in Uttaranchal University an invigorating experience.</br>
<br>I extend my warm welcome to our new entrants.</br>

<br>Jitender Joshi</br>
<br>Chancellor</p><br />
