<p class="p1">FAQ</p>
<p class="p2"><br>Who are our Alumni?</p>
<p class="p3">All individuals who have followed Diploma, Undergraduate, Postgraduate or PhD of the programmes of Uttaranchal University for the duration of at least one semester at uttaranchal University can register as our alumni.</p>
<p class="p2"><br>How do I become a member of the Uttaranchal UNiversity?</p>
<p class="p3">All undergraduates and post graduates of Uttaranchal UNiversity can register as our alumni. It is important for alumni to update their particulars for the Uttaranchal University Alumni Directory (Database) by register as a member online and pay at the counter of Alumni Uttaranchal University so that we can keep in touch with you for life.</p>
<p class="p2"><br>Do I need to pay any fee?</p>
<p class="p3">Yes, but it is only for membership registration. You can choose to pay yearly or pay for Life Membership. The fee for yearly paid and life membership are Uttaranchal University respectively. </p>
<p class="p2"><br>As an alumni, how can I contribute towards the University?</p>
<p class="p3">You can help by participating in the various activities organized by the University or by contributing ideas or programmes towards enriching learning experience of all students. Other assistance include donation, sponsorship, expertise and volunteer services. Whatever your contribution they are most welcome.</p>
<p class="p2"><br>I am still a student of the University, can I take part in alumni activities?</p>
<p class="p3">Yes, of course. We believed that the bond between alumni and students should begin as soon as the student enters the University because you will become our future alumni later on. The Alumni Advancement is also committed in organising training especially in soft skills and career development programmes to raise your employability in the job market upon graduation. We hope this will instill a pride in your alma mater and later on may wish to make whatever contribution towards achieving excellence for the University.
</p>
